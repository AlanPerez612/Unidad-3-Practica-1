    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        boolean respuesta = objCorri.eliminar();
        String mensaje = "Error, cola vacía.";
        if (respuesta) {
            mensaje = "Se eliminó correctamente.";
        }
        lblEstado.setText(mensaje);
        mostrarCola();
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnInsertarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInsertarActionPerformed
        char dato = txtDato.getText().charAt(0);
        int prio = cmbPrioridad.getSelectedIndex() + 1;
        boolean respuesta = objCorri.insertar(dato, prio);
        String mensaje = "Error, cola llena.";
        if (respuesta) {
            mensaje = "Se ingresó el dato: " + dato;
        }
        lblEstado.setText(mensaje);

        txtDato.setText("");

        mostrarCola();
    }//GEN-LAST:event_btnInsertarActionPerformed

