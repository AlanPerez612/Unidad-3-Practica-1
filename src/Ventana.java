    private void mostrarCola() {
        objCorri.ordenar();
        int prio = cmbPrioridad.getSelectedIndex() + 1;
        setTitle("Valores desde Ini: " + objCorri.getIni() + ", Fin:" + objCorri.getFin());
        if (objCorri.estaColaVacia()) {
            txtArea.setText("Cola vacía.");
            return;
        }
        String cad = "";
        for (int i = objCorri.getIni(); i <= objCorri.getFin(); i++) {
            cad += "[" + i + "]" + "=" + objCorri.getValor(i) + "---Prioridad---->" + prio + "\n";
        }
        txtArea.setText(cad);
    }

